export const CASE_COUNT_BY_REPORT_QUERY =
  'SELECT UCI.CLIENT_ID, UCI.ENTERPRISE_ID, UCI.CASE_REPORT_TYPE, COUNT(DISTINCT UCI.CASE_ID) FROM UAN_DWH_EDW.UAN_CASE_INFO UCI WHERE UCI.CASE_DELETED IS NULL AND UCI.CASE_STATE_ID <> 1 AND  UCI.ENTERPRISE_ID = ${enterpriseId} GROUP BY UCI.CLIENT_ID, UCI.ENTERPRISE_ID, UCI.CASE_REPORT_TYPE;';
