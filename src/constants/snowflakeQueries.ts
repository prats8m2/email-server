export const INSERT_ENTERPRISE =
  "INSERT INTO UAN_DWH_CNF.UAN_APP_TENANT_USER_MAPPING (CLIENT_ID, CLIENT_NAME, USER_ID, USER_NAME, TENANT_ID) values('${clientID}', '${clientName}', '${userId}', '${userName}', '${enterpriseId'})";

